/* StartUSARTTask function */
void StartUSARTTask(void const * argument)
{
  /* USER CODE BEGIN StartUSARTTask */
  /* Infinite loop */
  for(;;)
  {
    err=xSemaphoreTake(usart4SemHandle,portMAX_DELAY); 
    if(err==pdTRUE){
        HAL_UART_DMAStop(&huart4);  
        Udata.usart4Len=usart4MaxLen-hdma_uart4_rx.Instance->NDTR;
        HAL_UART_Transmit_DMA(&huart4, Udata.usart4Data, Udata.usart4Len);
        HAL_UART_Receive_DMA(&huart4, Udata.usart4Data, usart4MaxLen);        
    }
    osDelay(1); 
  }
  /* USER CODE END StartUSARTTask */
}

